let array = ['A','B','C','D','E','F'];
console.log("Array Before Join Operation ::: ");
console.log(array);

console.log("Array After Join Operation without arguments ::: ");
console.log(array.join());

console.log("Array After Join Operation with single quotes '' ::: ");
console.log(array.join(''));

console.log("Array After Join Operation with other characters ::: ");
console.log(array.join('R'));